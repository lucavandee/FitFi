## De Psychologie van Kledingkeuzes

Onderzoekers hebben decennia lang bestudeerd hoe kleding ons gedrag en onze perceptie beïnvloedt. Het fenomeen heet **"enclothed cognition"** - het idee dat wat we dragen letterlijk beïnvloedt hoe we denken en presteren.

Een beroemde studie toonde aan dat mensen die een witte jas droegen (en verteld werden dat het een doktersjas was) beter presteerden op aandachtstaken dan mensen in gewone kleding. Dezelfde jas, omschreven als "schildersjas", had geen effect. De betekenis die we aan kleding geven, verandert letterlijk onze cognitieve functies.

